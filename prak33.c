#include <stdio.h>
#include <stdlib.h>
int main(void){
 	int i, total=0, number=0;
 	printf("masukkan number: ");
 	scanf("%d",&number);
 	for (i=0;total=0;number==1) {
 		if(number < 0) {
 			continue;
 		}		
 			total += number;
 	}
	printf("Penjumlahan bilangan positif = %d\n", total);
 	return EXIT_SUCCESS;
}
